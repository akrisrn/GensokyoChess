      FileInputStream fi = new FileInputStream(file);
      BufferedInputStream fb = new BufferedInputStream(fi);
      Font nf = Font.createFont(Font.TRUETYPE_FONT, fb);
      nf = nf.deriveFont(Font.PLAIN, 16);
      Board.setFont(nf);
